    <!--  DIRECTIONS  -->
    <ol>
        <li>Cook onions and garlic in oil in a 4- to 5-quart heavy saucepan over moderately low heat 6 to 8 minutes, or until softened.</li>
        
        <li>Add tomatoes including juice, herbs, and sun dried tomatoes (if using). Bring to a boil, then reduce heat to a simmer. Continue simmering, partially covered, 45 minutes to an hour.</li>
        
    </ol>


