    <p id="intro">
    Use fresh home-grown tomatoes when possible, they make a great tasting sauce. If using canned tomatoes, you can
    use different kinds such as one of the "Italian style" canned concoctions that has basil, garlic and
    oregano.
    </p>

    <p id="intro">
    Fresh tomatoes should be easy to skin: cut the tomato in half and cut under the edge of the skin with a paring
    knife, then pull the skin off. If the skin does not come off easily, you can boil the tomato for about
    two minutes to loosen the skin.
    </p>

    <p id="intro">
    The ingredients that have a "+" next to the quantity is supposed to suggest a minimum quantity, and add more as you like.
