    <p>
    <b>PREP:</b> none; <b>Cooking time:</b> 30 min.
    </p>

    <p>Serves 4</p>

    <p id="intro">
    Polenta is much easier to make in a microwave, but it rises like bread and will spill if you don't watch it.
    </p>

    <p id="intro">
    Try adding some finely-chopped garlic to the polenta as it is cooking.
    </p>

    <p id="intro">
    If you make the polenta in a microwave, you can cut the recipe in
     half and use a big pyrex measuring cup or microwavable bowl. While microwaving it will rise; if
     it starts to overflow, take it out and stir it. You may have to do this a few times, but once
     most of the water has been absorbed it will stop rising.
    </p>

    <!--  INGREDIENTS  -->
    <div id="ingredients">

       <div class="listItem">
          <span class="ingredientAmount">1</span>
          <span class="ingredient">cup yellow corn meal</span>
       </div>

       <div class="listItem">
          <span class="ingredientAmount">5</span>
          <span class="ingredient">cups water</span>
       </div>

       <div class="listItem">
          <span class="ingredientAmount">1/4</span>
          <span class="ingredient">cup grated parmesan cheese</span>
       </div>

    </div>

    <!--  DIRECTIONS  -->
    <ol>
        <li><b>Stove top instructions:</b></li>
        <li>Heat water to boiling. Reduce heat to medium.</li>

        <li>Add corn meal, a little at a time, stirring constantly, until all is added.</li>

        <li>Continue stirring, breaking up lumps, 25 to 30 minutes.</li>

        <li><b>Microwave instructions:</b></li>
        
        <li>In a large pyrex measuring cup or microwavable bowl, add the water and all of the corn meal and stir.</li>

        <li>Microwave on high for 20 minutes, stirring about every 5 minutes or as needed to prevent overflow.</li>

        <li><b>When finished cooking:</b></li>

        <li>Pour the polenta onto a serving plate, or into the pockets of a cupcake tin. The polenta will
     be thick, so it helps to have an extra pair of hands - one pair to hold the pot and one to scoop out the polenta.</li>

        <li>Sprinkle parmesan cheese on top.</li>

    </ol>


