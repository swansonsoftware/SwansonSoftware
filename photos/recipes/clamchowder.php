    <!--  DIRECTIONS  -->
    <ol>
        <li>If using fresh clams: Rinse clams under cold water and put them in a steamer. Bring to a boil and steam the clams no more than four or five minutes. Rinse clams under cold water again. Save the steamer water, adjust it to 3 cups, and use it in step 3. Note: it is important to rinse the clams after steaming so they do not get overcooked and become tough. Continue to step 3.</li>

        <li>If not using fresh clams: Open cans of clams and strain juice into measuring cup. Add water until combined mixture equals three cups.</li>
        
        <li>Pour the combined liquid into a large stock pot and heat on medium. Add the potatoes and bay leaves.</li>

        <li>Pile the sliced leeks on top of the potatoes. They do not need to be submerged.</li>

        <li>Bring to a simmer. Gently push the leeks down occasionally as they soften.</li>

        <li>Simmer, partially covered, 1 hour.</li>

        <li>Off heat, pick the leeks and bay leaves out, and put them in a pot. Mash the leeks and strain out the liquid. Add the strained liquid back into the pot with the potatoes.</li>

        <li>Mash the potatoes in the soup, not too thoroughly.</li>

        <li>Reheat to a simmer, and let simmer 10 to 20 minutes to reduce the liquid, stirring occasionally.</li>

        <li>Let cool 15 minutes, then stir in the chopped clams.</li>

        <li>Slowly stir in the whipping cream.</li>

        <li>Add salt to taste.</li>

    </ol>


