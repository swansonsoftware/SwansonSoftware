<!--  DIRECTIONS  -->
<ol>
	<li>Pre-heat oven to 350 degrees.</li>

	<li>In mixing bowl, add eggs, sugar, and oil.  Beat with egg beater until light and fluffy.  Add vanilla.</li>

	<li>In a separate bowl, mix sifted flour with salt and baking powder. Fold this mixture into eggs, a little at a time.  When too thick to stir, continue to knead with hands until the texture is "right."</li>

	<li>Roll out dough as when making cookies.  Sprinkle on a generous amount of cinnamon and sugar, then roll up into a log.  Cut into a shape that will fin in a 5-inch W by 9-inch L by 2.5-inch D bread pan.</li>

	<li>Bake until crust starts to brown.</li>
</ol>

