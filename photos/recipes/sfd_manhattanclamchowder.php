<!--  DIRECTIONS  -->
<ol>
	<li>If using fresh clams see step 7 for preparation, otherwise continue to step 2.</li>

	<li>Open cans of clams and strain juice into measuring cup. Add water until combined mixture equals three cups. Heat until simmering.</li>

	<li>In a large pot, saute garlic and onions in oil and butter until onions are transluscent. Add carrots and celery and saute for three or four minutes.</li>

	<li>Add the tomatoes, heated clam juice and water, potatos, and bay leaves. Bring to a boil. Reduce heat and simmer 1 to 1&frac14 hour.</li>

	<li>Add tarragon, thyme, pepper, and cilantro. Stir into soup.</li>

	<li>Remove the pot from the heat and let cool about 10 minutes. Stir in clams.</li>

	<li>If using fresh clams: rinse clams under cold water and put them in a steamer. Bring to a boil and steam the clams no more than four or five minutes. Rinse clams under cold water again. Save the steamer water and use it in step 4. Note: it is important to rinse the clams after steaming so they do not get overcooked and become tough. Continue to step 3.</li>
</ol>


