<!--  INTRODUCTION  -->
<p>
<b>PREP:</b> 15 min; <b>Cooking time:</b> 10 min
</p>

<p>Serves 2</p>

<p id="intro">
The foundation of this egg dish is tomato and basil, though you can substitute
ingredients to suite the day.
</p>

<!--  INGREDIENTS  -->
<div id="ingredients">

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">tablespoon butter (or cooking oil)</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">green onions, chopped (substitute &frac14; cup chopped yellow onion)</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">small tomato, chopped (substitute &frac14; cup chopped red bell pepper)</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">4</span>
	  <span class="ingredient">leaves fresh basil, chopped, or 1 teaspoon dried basil leaves</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">6</span>
	  <span class="ingredient">eggs, beaten</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">+</span>
	  <span class="ingredient">Freshly ground pepper</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">+</span>
	  <span class="ingredient">Salt to taste</span>
   </div>

</div>

<!--  DIRECTIONS  -->
<ol>
	<li>Melt butter in a pan over medium-low heat. Add onions (and bell pepper, if using) and cook until soft, about 4 minutes. Stir in tomato and basil, and  cook until heated through.</li>

	<li>Pour in beaten eggs and mix a little. Let cook without stirring.</li>

	<li>As eggs begin to set, use a spatula and push from edge towards center of pan so that uncooked egg can flow in.  Do not use a pan that food sticks to, if you care about the texture of the eggs.  A well-seasoned cast iron pan works great.  When the set egg is pushed with the spatula, it should lift from the pan and fold.  Cook until eggs are thickened but still moist.  Season to taste with salt and pepper.</li>
</ol>


