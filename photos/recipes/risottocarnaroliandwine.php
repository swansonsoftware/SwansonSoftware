    <!--  INTRODUCTION  -->
    <p>
    <b>PREP:</b> 10 min; <b>Cooking time:</b> 18 to 20 min
    </p>

    <p>Serves 4</p>

    <p id="intro">
    This simple Risotto makes a good side dish for almost anything.
    </p>

    <p id="intro">
    My favorite risotto rice is carnaroli, it makes a creamier risotto. It may be difficult to find. Fior di Riso carnaroli rice is very good. Risotto needs a white wine. Any good white wine will work, but for my taste I tend to pick a Chardonnay.
    </p>

    <!--  INGREDIENTS  -->
