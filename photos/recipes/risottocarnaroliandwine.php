    <!--  DIRECTIONS  -->
    <ol>
        <li>Heat the broth to a simmer, and keep it simmering.</li>

        <li>Heat 1 tablespoon butter in your favorite risotto pan over medium heat, then saute the onions and garlic and cook until soft but not brown - about 5 minutes.</li>

        <li>Spoon the sauted vegetables into a bowl and set aside. Put the pan back on the stove, and add the remaining butter. When the butter has melted, add the rice and stir constantly for about 2 minutes. The point is to heat the grains and coat them with the butter.</li>

        <li>Add the wine, stir a little, and let the rice absorb some of the liquid. Add 1 ladle of the hot chicken broth. The rice should not be submerged beneath lots of chicken broth - add enough broth so the liquid is near the surface of the rice grains.</li>

        <li>The broth should bubble. When most of the broth has been absorbed by the rice, add another ladle from the simmering broth and stir.</li>
        
        <li>Continue the above, ladling in more broth as the risotto cooks and absorbs the broth. When a few ladles of broth are left, add the sauted vegetables to the risotto. Add the sun dried tomatoes if using.</li>

        <li>Continue the  above until one ladle of the broth remains, or for about 18 to 20 minutes. The rice should be creamy when done.</li>

        <li>Remove the risotto from heat. Stir in the parmigian cheese.</li>
    </ol>


