<!--  INGREDIENTS  -->
<div id="ingredients">

   <div class="listItem">
      <span class="ingredientAmount">4</span>
	  <span class="ingredient">boneless skinless chicken breast halves</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">tablespoon butter</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">4</span>
	  <span class="ingredient">small red potatoes (substitute 14 oz. package frozen vegetables)</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">large carrots (or 2 handfuls baby carrots), coarsley chopped</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">&#189;</span>
	  <span class="ingredient">teaspoon salt</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">&#189;</span>
	  <span class="ingredient">teaspoon fresh ground black pepper</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">&#190;</span>
	  <span class="ingredient">cup water</span>
   </div>

</div>

<!--  DIRECTIONS  -->
<ol>
	<li>Microwave or bake potatoes until fully-cooked.</li>

	<li>While potatoes are cooking, sprinkle chicken with salt and
		pepper. Melt butter in skillet over medium heat, and place chicken breasts flat in skillet.
		Cook chicken 6 minutes per side, turning once.  Check that chicken is done by making a
		small cut in thickest piece: juice should not be pink. Remove chicken from pan.</li>
	
	<li>Cut potatoes into quarters or eighths. Add water, cut
		potatoes and carrots to skillet. Mix and let cook until carrots are tender, about five
		minutes.  Add chicken, and simmer about 2 minutes to heat chicken.</li>
</ol>


