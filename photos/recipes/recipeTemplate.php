<!--  DIRECTIONS  -->
<ol>
	<li>You can roast the chilis 10 minutes or use them raw. Roast the garlic 15 minutes in a pan or on a skillet over medium heat (or microwave with skin on for 15 seconds).</li>

	<li>Remove the stem from the peppers and scrape off any burnt skin. Roughly chop the peppers and put in the a bowl.</li>

	<li>Peel and chop the garlic, and put in the bowl with the peppers.</li>

	<li>Add tomatoes and their juice to the bowl. Stir in onions, cilantro, and lime juice.</li>
</ol>


