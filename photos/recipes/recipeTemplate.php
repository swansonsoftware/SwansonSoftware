<p>
<b>PREP:</b> 15 min; <b>Cooking time:</b> none
</p>

<p>Makes 4 Servings</p>

<p id="intro">
You can make this recipe with fresh or dried fruit instead of tomatoes. Instead of jalapeno pepper, you can use other kinds of peppers or use dried red chilis prepared by heating on a dry skillet, then soaking in water, remove seeds and stems and liquefying in a blender.
</p>

<p id="intro">
When I make this recipe with fruit I use red onion and no garlic. My favorites for this recipe are persimmon or mango.
</p>

