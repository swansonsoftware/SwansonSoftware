<!--  DIRECTIONS  -->
<ol>
	<li>Heat the oil (and butter if using) in a large, heavy-bottomed saucepan. Add the garlic, saute for 2 minutes, then add the remaining ingredients, a little at  a time, stirring constantly and cooking until softened but not browned.</li>

	<li>Add 2-1/2 quarts of water and bring to a boil. Reduce the heat, cover, and simmer for 2 hours. Let cool 3 hours then strain the stock. Discard the solids.</li>

	<li>Return the stock to the pan and bring to a simmer; simmer for 15 minutes. Increase heat, bringing the stock to a boil, and boil until reduced by half.</li>

    <li>Use as needed.</li>

</ol>


