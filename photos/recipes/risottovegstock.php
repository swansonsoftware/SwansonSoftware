      <span class="ingredientAmount">4</span>
	  <span class="ingredient">tablespoons olive oil</span>
	  <br />
      <span class="ingredientAmount">&nbsp;</span>
	  <span class="ingredient">(or 2 tablespoons unsalted butter and 2 tablespoons olive oil)</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">whole garlic flower, cloves separated, peeled, and crushed</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">large onion, coarsely chopped</span>
