<p>
<b>PREP:</b> 5 min; <b>Cooking time:</b> 15 min
</p>

<p>Serves 4</p>

<p id="intro">
You can substitute a small (4.5 oz) can of chopped green chiles for fresh.
</p>

<!--  INGREDIENTS  -->
<div id="ingredients">

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">package burrito-style flour tortillas</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">cups shredded cheddar cheese</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">cups pulled or chopped chicken</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">poblano pepper, roasted, steamed or boiled until soft</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">tablespoon butter</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">cup salsa</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">1</span>
	  <span class="ingredient">avacado, halved, with pit and skin removed</span>
   </div>

</div>

<!--  DIRECTIONS  -->
<ol>
	<li>Heat 12-inch skillet over medium-low heat. While skillet is heating, slice the poblano pepper into thick slivers, discarding seeds.</li>

	<li>Cook one tortilla	until lightly browned on one side, then turn over and brush with butter.</li>

	<li>Sprinkle some cheese on the tortilla, add some chicken, a few slivers of the poblano, and salsa. Let the bottom tortilla cook until golden brown, then  cover with another tortilla and flip the quesadilla over. Press down on the quesadilla (with your hands or with a plate) to get the cheese to stick and to remove air pockets.</li>

	<li>Cook until the bottom tortilla is golden brown. Remove from the heat and cut into wedges. Garnish with avacado.</li>
</ol>


