    <!--  DIRECTIONS  -->
    <ol>
        <li>Preheat oven to 300 degrees F.</li>

        <li>Mix the dry ingredients (except any dried or freeze-dried fruit) in a large bowl.</li>
        
        <li>Mix the liquid ingredients thoroughly in a measuring cup. Pour over the mixed dry ingredients. Pour it all at once, then mix just enough to coat everything and that will produce some small clumps when cooked. If you don't want clumps, then pour the liquid in 1/4 cup at a time, mixing to evenly coat everything.</li>
        
        <li>Spread the mixture evenly on two 9x13-inch baking pans. Bake 30 minutes, then stir.
            Bake 20 minutes more, then stir again. Bake 10 minutes more, then stir again. Repeat this 10 minute interval until the granola
            is golden brown, then remove from oven and let cool.</li>
        
        <li>Add the freeze-dried fruit to the cooled mixture. Stir, then store in an air-tight container.</li>
    </ol>


