<!--  INGREDIENTS  -->
<div id="ingredients">

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">tablespoons olive oil or butter</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">6+</span>
	  <span class="ingredient">garlic cloves, finely chopped</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">pounds uncooked medium shrimp, peeled and deveined</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">4</span>
	  <span class="ingredient">large carrots, cut into julienne strips</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">2</span>
	  <span class="ingredient">green onions, chopped</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">&frac14</span>
	  <span class="ingredient">cup cilantro, chopped</span>
   </div>

   <div class="listItem">
      <span class="ingredientAmount">+</span>
	  <span class="ingredient">1 teaspoon red pepper flakes (optional)</span>
   </div>

</div>

<!--  DIRECTIONS  -->
<ol>
	<li>Add the oil to a large skillet and saute garlic and carrots over medium heat for four or five minutes.</li>

	<li>Add shrimp, green onions, and red pepper flakes, stir-fry 5 to 10 minutes or until shrimp are pink and firm and carrots are almost tender. Stir in cilantro.</li>

	<li>Serve over rice.</li>
</ol>


