<p>
Some of the features I want to point out:
</p>

<ul>
    <li>Column headers on tables execute a client-side sort of the grid on that column, alternately ascending/descending.</li>
    <li>Query Builder - allows ad hoc reporting, with ability to save report definition as public or private.</li>
    <li>My Calls - role-based views.</li>
    <li>Email notification - users can be notified by email when a call is assigned.</li>
    <li>Email response to customer - built-in functionality, and responses are saved with the call.</li>
    <li>Knowledge Base - full-featured knowledge base that enables customer self-help.</li>
    <li>Full Web-based administration - user maintenance, report layout, and field values can be added, removed, and changed using the Admin page.</li>
    <li>Help - complete system documentation is available and presented in HTML format.</li>
</ul>
